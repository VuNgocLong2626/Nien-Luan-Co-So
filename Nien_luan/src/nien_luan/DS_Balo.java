    //Thêm Cái Balo vào MySQL
    public void themBalo(){
        try {
            ClsDatabase.open();
            String sql = new String("insert into balo(mso, kluong, loai, soluongdv) values (?,?,?,?)");
            PreparedStatement ps = ClsDatabase.con.prepareCall(sql);
            
            ps.setString(1, txtMSBalo.getText());
            ps.setFloat(2, Float.parseFloat(txtTKLuong.getText()));
            ps.setInt(3, getloai());
            ps.setInt(4, 0);
            
            ps.executeUpdate();
            ClsDatabase.close();
            JOptionPane.showMessageDialog(this, "Thêm balo thành công!");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    //Hiển thị tất cả Balo
    public void showBalo(){
        DefaultTableModel tblModel = (DefaultTableModel) tableDS_Balo.getModel();
        tblModel.setRowCount(0);//reset view jtable
        try {
            ClsDatabase.open();
            Statement s = ClsDatabase.con.createStatement();
            ResultSet rs = s.executeQuery("select * from balo");
            while (rs.next()) {
                String id = rs.getString(1);
                String maso = rs.getString(2);
                String diemS = rs.getString(3);
                String loai = rs.getString(4);
                String soluongdv = rs.getString(5);

                String Data[] = {id, maso, diemS, loai, soluongdv};
                tblModel.addRow(Data);
            }
            ClsDatabase.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    //Cập nhập Balo
    public void updateBalo(){
        try {
            ClsDatabase.open();
            String url = "update balo set mso = ?, kluong = ?, loai = ? where id = ? ";
            PreparedStatement ps = ClsDatabase.con.prepareStatement(url);
            
            ps.setString(1, txtMSBalo.getText());
            ps.setFloat(2, Float.parseFloat(txtTKLuong.getText()));
            ps.setInt(3, getloai());
            ps.setInt(4, this.id);
            
            ps.executeUpdate();
            ClsDatabase.close();
            JOptionPane.showMessageDialog(this, "Cập nhật balo thành công!");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
