    private void tableDS_BaloMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableDS_BaloMouseClicked
        // TODO add your handling code here:
        int row = tableDS_Balo.getSelectedRow();
        TableModel model = tableDS_Balo.getModel();
        
        this.id = Integer.parseInt(model.getValueAt(row, 0).toString());
        txtMSBalo.setText(model.getValueAt(row, 1).toString());
        txtTKLuong.setText(model.getValueAt(row, 2).toString());
        int loai = Integer.parseInt(model.getValueAt(row, 3).toString());
        if(loai == 1)
            rdbLoai1.setSelected(true);
        if(loai == 2)
            rdbLoai2.setSelected(true);
        if(loai == 3)
            rdbLoai3.setSelected(true);
        this.sluong = Integer.parseInt(model.getValueAt(row, 4).toString());
        this.resetButton(false);
    }//GEN-LAST:event_tableDS_BaloMouseClicked

