    //Xoa Balo
    public void XoaBalo(){
        try {
            ClsDatabase.open();
            String url = "delete from balo where id = ?";
            PreparedStatement ps = ClsDatabase.con.prepareStatement(url);
            
            ps.setInt(1, this.id);
            ps.executeUpdate();
            ClsDatabase.close();
            JOptionPane.showMessageDialog(this, "Xóa balo thành công!");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
